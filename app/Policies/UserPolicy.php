    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
        return $user->hasPermissions(['user_create']);
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function update(User $user, User $model)
    {
        if( $user->hasPermissions(['user_update'])) {
            return true;
        } else {
            return $user->hasPermissions(['user_self_update'], $model);
        }
    }

    /**
     * Determine whether the user can delete the model.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function delete(User $user, User $model)
    {
        if( $user->hasPermissions(['user_remove'])) {
            return true;
        } else {
            return $user->hasPermissions(['user_remove_self'], $model);
        }
    }

